<h3 id="suitable" tabindex="-1"><a class="header-anchor" href="#suitable" aria-hidden="true">#</a> Jmix 适合什么项目和开发者？</h3>
<p>Jmix 的使命是简化开发业务线 Web 应用系统，并提供卓越的生产力。这些业务系统是在公司、区域或部门级别运行。此类型的系统范围很广，从电子表格到高度专业化的自定义系统，分为 ERP、CRM、EAM、SCM、WMS、ECM、ERM、HRM 等。这类系统的典型特征是：</p>
<ul>
<li>具有重要的业务逻辑或需要流程自动化</li>
<li>有大量的数据模型</li>
<li>统一且功能化的 UI，且 UI 界面数量较多</li>
<li>用户数量可预测，一般是公司内部职员或合作伙伴</li>
</ul>
<p>Jmix 是为专业开发者打造的产品，来源于开发者，服务于开发者。仍在使用旧技术栈的软件工程师或者只有 Java 基础知识的新手都可以使用Jmix，并且也能非常高效！Jmix 定义了应用程序的架构，并能指导您轻松地部署应用程序。因此，适合下列类型的团队：</p>
