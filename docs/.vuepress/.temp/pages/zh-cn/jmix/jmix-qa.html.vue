</ul>
<p>另外，Jmix 也适合下列类型的项目：</p>
<ul>
<li>作为面向大量用户系统的具有高级别管理界面的强大后端</li>
<li>对老旧系统进行现代化改造（Jmix 支持数据库反向工程）</li>
<li>内部流程和工作流自动化（Jmix 有 BPM 引擎）</li>
<li>避免 Saas/低代码 供应商费用和限制</li>
</ul>
<h3 id="safety" tabindex="-1"><a class="header-anchor" href="#safety" aria-hidden="true">#</a> Jmix 开发的项目安全性如何？</h3>
<p>一些怀疑论者声称开源软件不如专有软件安全。但这只是一个理论假设。开源代码的关键差异化优势在于，会有远远超出一个小团队数量的眼睛盯着代码改动。由于对代码有完全的访问权，因此可以做深度的代码检查从而防止应用程序的突然崩溃，开源的代码没有“黑匣子”。</p>
<p>从 2016 年开始，CUBA 平台作为开源项目进行开发。平台团队持续发布公开透明的产品路线图，并且通过公共的 GitHub 仓库定期管理问题跟踪。Jmix 平台继承自 CUBA 平台，代表了一个基于面向未来的技术栈的成熟开源项目。</p>
<p>平台的成熟度不能保证排除了所有的安全风险，所以仍然需要强制的安全保障程序。但拥有超过 25,000 名成员和 1000 多个使用该平台完成的项目的强大社区为构建安全应用程序奠定了坚实的基础。</p>
